
def add(a,b):
    """
    Simple method that adds two parameters

    Parameters:
    - a: a number
    - b: a number

    Returns:
    - a+b: The sum of a and b.
    """
    return a + b

def sub(a,b):
    """
    Simple method that subtracts two parameters

    Parameters:
    - a: a number
    - b: a number

    Returns:
    - a-b: The sum of a and b.
    """
    return a - b

def mult(a,b):
    """
    Simple method that multiplies two parameters

    Parameters:
    - a: a number
    - b: a number

    Returns:
    - a*b: The product sum of a and b.
    """
    return a*b

def div(a,b):
    """
    Simple method that divide two parameters

    Parameters:
    - a: a number
    - b: a number

    Returns:
    - a/b: The division of a over b.
    """
    return a/b

def pow(a,b):
    """
    Simple method that take one parameter to the power of the other

    Parameters:
    - a: a number
    - b: a number

    Returns:
    - a-b: The sum of a and b.
    """
    return a**b